const geistSans = Geist({
  variable: "--font-geist-sans",
  subsets: ["latin"],
});

const geistMono = Geist_Mono({
  variable: "--font-geist-mono",
  subsets: ["latin"],
});

const playfairDisplay = Playfair_Display({
  variable: "--font-geist-serif",
  subsets: ["latin"],
});

export const metadata: Metadata = {
  title: "Create Next App",
  description: "Generated by create next app",
};

export default async function RootLayout({
  children,
}: Readonly<{
  children: React.ReactNode;
}>) {
  console.log("[LAYOUT] RootLayout rendering...");
  const supabase = await createClient();
  const {
    data: { user },
  } = await supabase.auth.getUser();
  console.log("[LAYOUT] getUser result:", { hasUser: !!user, email: user?.email });

  // Get user roles for sidebar filtering
  const userRoles = user ? await getUserRoles() : null;
  console.log("[LAYOUT] userRoles:", userRoles);

  type TenantRow = {
    tenant_id: string;
    tenants: { name: string | null } | null;
  };
  let tenantRows: TenantRow[] | null = null;
  if (user) {
    const { data } = await supabase
      .from("memberships")
      .select("tenant_id, tenants(name)")
      .eq("user_id", user.id)
      .order("created_at", { ascending: true });
    tenantRows = (data ?? null) as TenantRow[] | null;
  }
  const tenants =
    tenantRows?.map((row) => ({
      id: row.tenant_id,
      name: row.tenants?.name ?? "Organizaci√≥n",
    })) ?? [];

  return (
    <html lang="en">
      <head>
        <script
          crossOrigin="anonymous"
          src="https://unpkg.com/react-scan/dist/auto.global.js"
        ></script>
        {/* rest of your scripts go under */}
      </head>
      <body
        className={`${geistSans.variable} ${geistMono.variable} ${playfairDisplay.variable} antialiased`}
      >
        <QueryClientProvider>
          <SupabaseAuthListener />
          <AuthController />
          <Toaster position="bottom-right" richColors />
          <NotificationsListener />
          <SidebarProvider>
            <AppSidebar user={user} userRoles={userRoles} tenants={tenants} />
            <SidebarInset>
            <header className="flex h-12 max-w-full shrink-0 items-center gap-4 border-b px-4">
              <div className="flex items-center gap-3 min-w-0">
                <SidebarTrigger className="-ml-1" />
                <PageBreadcrumb />
              </div>
              <div className="flex items-center gap-2 ml-auto">
                <ImpersonateBanner />
                <UserMenu email={user?.email} userRoles={userRoles} />
              </div>
            </header>
            {/* <div className="px-4 pt-4">
              <PendingInvitationsBanner />
            </div> */}
            <main className="flex flex-1 flex-col gap-4">{children}</main>
            </SidebarInset>
          </SidebarProvider>
        </QueryClientProvider>
      </body>
    </html>
  );
}
