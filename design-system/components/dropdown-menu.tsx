export interface DropdownMenuContentProps extends React.ComponentPropsWithoutRef<typeof DropdownMenuPrimitive.Content> {
  heading?: React.ReactNode
  hideHeading?: boolean
}

export const DropdownMenuContent = React.forwardRef<
  React.ElementRef<typeof DropdownMenuPrimitive.Content>,
  DropdownMenuContentProps
>(({ className, children, heading = "Actions", hideHeading = false, sideOffset = 6, ...props }, ref) => {
  const showHeading = !hideHeading && heading !== null && heading !== false

  return (
    <DropdownMenuPrimitive.Portal>
      <DropdownMenuPrimitive.Content
        ref={ref}
        sideOffset={sideOffset}
        className={cn(
          "z-dropdown min-w-40 overflow-hidden rounded-md border border-stroke bg-surface p-1 text-content shadow-dropdown data-[state=open]:animate-in data-[state=closed]:animate-out data-[state=closed]:fade-out-0 data-[state=open]:fade-in-0",
          className
        )}
        {...props}
      >
        {showHeading ? (
          <>
            <DropdownMenuPrimitive.Label className="px-2 py-1.5 text-sm font-semibold text-content">
              {heading}
            </DropdownMenuPrimitive.Label>
            <DropdownMenuPrimitive.Separator className="-mx-1 my-1 h-px bg-stroke-soft" />
          </>
        ) : null}
        {children}
      </DropdownMenuPrimitive.Content>
    </DropdownMenuPrimitive.Portal>
  )
})
DropdownMenuContent.displayName = DropdownMenuPrimitive.Content.displayName

